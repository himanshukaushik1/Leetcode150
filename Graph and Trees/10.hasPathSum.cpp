bool hasPathSum(Node *node, int sum) {
    if(!node)
        return false;
    if(node->data == sum){
        if(!node->left and !node->right)
            return true;
        return false;
    }
        
    else if(node->data > sum)
        return false;
    return hasPathSum(node->left, sum - (node->data)) || hasPathSum(node->right, sum-(node->data));
    
}